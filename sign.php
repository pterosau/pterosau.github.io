  <script>
    // Image carousel functionality
    const images = document.querySelectorAll('#phone-images img');
    let currentImageIndex = 0;

    function switchImage() {
      images[currentImageIndex].classList.remove('visible');
      images[currentImageIndex].classList.add('hidden');

      currentImageIndex = (currentImageIndex + 1) % images.length;

      images[currentImageIndex].classList.remove('hidden');
      images[currentImageIndex].classList.add('visible');
    }

    setInterval(switchImage, 2500); // Switch images every 2.5 seconds
  </script>
